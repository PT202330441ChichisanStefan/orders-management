package org.stefan.tuc.model;


/**
 The ParentModel class represents a parent model in the application.
 It provides attributes for storing an ID and a name.
 @author Chichisan Stefan
 */
public class ParentModel {

    public int id1;
    public String name1;

    public ParentModel() {}
    public ParentModel(int id, String name) {
        id1 = id;
        name1 = name;
    }
}
